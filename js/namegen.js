// --- Utilities ---

function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function combine(slots) {
  return slots.map(slot => pick(slot)).join("");
}

function cap(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

// --- Markov Engine ---

function markovName(cfg) {
  const { table, minLen, maxLen, fallbacks } = cfg;
  let attempts = 0;
  while (attempts < 100) {
    let state = pick(table["^"]);
    let name = state;
    let steps = 0;
    while (steps < 20) {
      const successors = table[state];
      if (!successors) break;
      const token = pick(successors);
      if (token === ".") break;
      name += token[1];
      state = token;
      steps++;
    }
    const capitalised = name[0].toUpperCase() + name.slice(1).toLowerCase();
    if (capitalised.length >= minLen && capitalised.length <= maxLen) {
      return capitalised;
    }
    attempts++;
  }
  const fallback = pick(fallbacks);
  return fallback[0].toUpperCase() + fallback.slice(1).toLowerCase();
}

