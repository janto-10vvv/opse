// --- Generators ---

function generatePersonName(culture, variant) {
  const data = CULTURES[culture];
  if (!data) {
    setStatus("Unknown culture: " + culture);
    return null;
  }

  if (culture === "human") {
    const variantData = data[variant];
    if (!variantData) {
      setStatus("Unknown variant: " + variant);
      return null;
    }
    if (variant === "surname") {
      return "> 👤 Human Surname: " + markovName(variantData);
    }
    const first = markovName(variantData);
    const surname = markovName(data.surname);
    const label = variant === "male" ? "Human Male" : "Human Female";
    return "> 👤 " + label + ": " + first + " " + surname;
  }

  if (culture === "dwarf") {
    const personal = markovName(data.name);
    const clan = markovName(data.clan);
    return "> 👤 Dwarf: " + personal + " " + clan;
  }

  // All other cultures: single name pool
  const label = culture.charAt(0).toUpperCase() + culture.slice(1);
  return "> 👤 " + label + ": " + markovName(data.name);
}

function generatePlaceName(subtype) {
  switch (subtype) {
    case "town":
      return "> 🏰 Place (Town): " + combine([COMBINE.place.town.prefix, COMBINE.place.town.suffix]);
    case "building":
      return "> 🏰 Place (Building): The " + combine([COMBINE.place.building.adjective, [" "], COMBINE.place.building.type]);
    case "geographic":
      return "> 🏰 Place (Geographic): " + combine([COMBINE.place.geographic.descriptor, [" "], COMBINE.place.geographic.feature]);
    default:
      return null;
  }
}

function generateGuildName() {
  return "> 📜 Guild: The " + combine([COMBINE.guild.adjective, [" "], COMBINE.guild.symbol]);
}

function generateItemName(subtype) {
  if (subtype === "weapon" || subtype === "armour") {
    var data = COMBINE.item[subtype];
    var base = combine([data.material, [" "], data.type]);
    if (Math.random() < 0.3) {
      return "> ⚔️ Item (" + cap(subtype) + "): " + base + " of the " + pick(data.epithet);
    }
    return "> ⚔️ Item (" + cap(subtype) + "): " + base;
  }
  if (subtype === "potion") {
    return "> ⚔️ Item (Potion): " + combine([COMBINE.item.potion.form, [" of "], COMBINE.item.potion.ingredient]);
  }
  return null;
}

